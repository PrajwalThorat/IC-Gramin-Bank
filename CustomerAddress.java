public class CustomerAddress {

    private int doorNo;
    private String street;
    private String landMarks;
    private String villege;
    private String city;
    private int pincode;

    
    public CustomerAddress(int doorNo, String street, String landMarks, String villege, String city, int pincode) {
        this.doorNo = doorNo;
        this.street = street;
        this.landMarks = landMarks;
        this.villege = villege;
        this.city = city;
        this.pincode = pincode;
    }
    

    
}
